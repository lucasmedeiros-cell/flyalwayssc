/* ---------------- Detalle (documentos + timeline) ------------------ */

function buildFiles(c: Customer): CustomerFile[] {
  const files: CustomerFile[] = [];
  if (c.passportNumber) {
    files.push({
      id: `${c.id}-f-pass`,
      kind: "passport",
      name: `Pasaporte ${c.passportNumber}.pdf`,
      fileType: "pdf",
      url: "#",
      sizeKb: 420,
      uploadedAt: c.createdAt,
      expiresAt: c.passportExpiry,
    });
  }
  files.push({
    id: `${c.id}-f-id`,
    kind: "id",
    name: "Cédula de identidad.jpg",
    fileType: "image",
    url: "#",
    sizeKb: 180,
    uploadedAt: c.createdAt,
  });
  if (c.tripsCount > 0) {
    files.push({
      id: `${c.id}-f-inv`,
      kind: "invoice",
      name: "Factura última compra.pdf",
      fileType: "pdf",
      url: "#",
      sizeKb: 96,
      uploadedAt: c.lastActivityAt ?? c.createdAt,
    });
  }
  return files;
}

function buildActivity(c: Customer): CustomerActivity[] {
  const acts: CustomerActivity[] = [
    {
      id: `${c.id}-a-created`,
      kind: "note",
      title: "Cliente registrado",
      detail: c.notes,
      at: c.createdAt,
      agent: c.assignedAgentName,
    },
  ];
  if (c.tripsCount > 0) {
    acts.push(
      {
        id: `${c.id}-a-quote`,
        kind: "quote",
        title: "Cotización enviada",
        detail: `Destino ${c.favoriteDestinations[0] ?? "internacional"}`,
        at: "2026-06-10T10:00:00Z",
        agent: c.assignedAgentName,
      },
      {
        id: `${c.id}-a-booking`,
        kind: "booking",
        title: "Reserva confirmada",
        detail: `${c.favoriteAirlines[0] ?? "Aerolínea"} · ${c.favoriteDestinations[0] ?? ""}`.trim(),
        at: "2026-06-12T15:30:00Z",
        agent: c.assignedAgentName,
        amount: { amount: Math.round(c.totalSpent.amount / Math.max(c.tripsCount, 1)), currency: c.totalSpent.currency },
      },
      {
        id: `${c.id}-a-payment`,
        kind: "payment",
        title: "Pago recibido",
        detail: "Método: QR",
        at: "2026-06-12T16:00:00Z",
        agent: "Sistema",
        amount: { amount: Math.round(c.totalSpent.amount / Math.max(c.tripsCount, 1)), currency: c.totalSpent.currency },
      },
    );
  }
  acts.push({
    id: `${c.id}-a-call`,
    kind: "call",
    title: "Llamada de seguimiento",
    detail: "Confirmación de itinerario y preferencias.",
    at: c.lastActivityAt ?? c.createdAt,
    agent: c.assignedAgentName,
  });
  // Orden cronológico descendente.
  return acts.sort((a, b) => (a.at < b.at ? 1 : -1));
}

export function findCustomer(id: string): CustomerDetail | null {
  const c = MOCK_CUSTOMERS.find((x) => x.id === id);
  if (!c) return null;
  return { ...c, files: buildFiles(c), activity: buildActivity(c) };
}
